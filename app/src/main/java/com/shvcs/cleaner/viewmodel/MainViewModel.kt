        elm = btElm
        viewModelScope.launch {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.CONNECTING,
                connectionMode = ConnectionMode.BLUETOOTH,
                btDeviceName = device.name ?: device.address,
                logs = _state.value.logs + "Connecting via Bluetooth to ${device.name ?: device.address}...",
                errorMessage = null
            )
            val result = btElm.connect(device)
            result.onSuccess {
                _state.value = _state.value.copy(
                    logs = _state.value.logs + "✓ Bluetooth connected to ELM327"
                )

                // Auto-init and read vehicle data
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.RUNNING,
                    progressStep = "Initializing..."
                )
                val initOk = OBDProtocol.executeInitAndReadData(elm, createListener())

                if (initOk) {
                    _state.value = _state.value.copy(
                        connectionState = ConnectionState.CONNECTED,
                        progress = 1f,
                        progressStep = "Ready"
                    )
                    scanBattery()
                } else {
                    _state.value = _state.value.copy(
                        connectionState = ConnectionState.CONNECTED,
                        progress = 0f,
                        progressStep = "Init failed",
                        errorMessage = "ELM327 initialization failed — try again or check connection"
                    )
                }
            }.onFailure { e ->
                elm.disconnect()
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.DISCONNECTED,
                    logs = _state.value.logs + "✗ Bluetooth connection failed: ${e.message}",
                    errorMessage = "Bluetooth failed: ${e.message}"
                )
            }
        }
    }

    /**
     * Set the connection mode (WiFi or Bluetooth) — used before connect.
     */
    fun setConnectionMode(mode: ConnectionMode) {
        elm = when (mode) {
            ConnectionMode.WIFI -> wifiElm
            ConnectionMode.BLUETOOTH -> btElm
        }
        _state.value = _state.value.copy(connectionMode = mode)
    }

    fun disconnect() {
        viewModelScope.launch {
            elm.disconnect()
            _state.value = _state.value.copy(
                connectionState = ConnectionState.DISCONNECTED,
                vin = "—", voltage = "—", seed = "—",
                progress = 0f, progressStep = "",
                currentStep = 0, totalSteps = 0,
                errorMessage = null
            )
        }
    }

    fun startClearSequence(testOnly: Boolean = false) {
        val cs = _state.value.connectionState
        if (cs != ConnectionState.CONNECTED && cs != ConnectionState.SUCCESS && cs != ConnectionState.ERROR) return

        viewModelScope.launch {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.RUNNING,
                progress = 0f,
                seed = "—",
                errorMessage = null,
                ecuResponse = null,
                ecuResponseCategory = UdsResponse.Category.NEUTRAL,
                pendingDtcClear = false,
                pendingKeyTest = testOnly,
                autoGeneratedKey = null,
                logs = _state.value.logs + "" + if (testOnly) "━━━ STARTING KEY TEST SEQUENCE ━━━" else "━━━ STARTING SHVCS CLEAR SEQUENCE ━━━"
            )

            val seed = OBDProtocol.executeUntilSeed(elm, createListener())

            if (seed != null) {
                // Seed received — wait for user key input
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.AWAITING_KEY,
                    progressStep = "Enter security key"
                )
            }
            // If seed is null, listener.onError already set ERROR state
        }
    }

    /**
     * User submits a key from the dialog.
     * Routes to either the SHVCS clear or DTC clear flow depending on pendingDtcClear.
     */
    fun submitKey(key: String) {
        val upperKey = key.uppercase()
        if (!OBDProtocol.isValidKey(upperKey)) {
            _state.value = _state.value.copy(
                errorMessage = "Invalid key. Enter exactly 4 hex characters (0-9, A-F)",
                logs = _state.value.logs + "✗ Invalid key format: $key"
            )
            return
        }

        viewModelScope.launch {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.UNLOCKING,
                errorMessage = null,
                logs = _state.value.logs + "Submitting key: $upperKey"
            )

            if (_state.value.pendingDtcClear) {
                // DTC clear flow: unlock + 14 FF FF FF + re-read
                _state.value = _state.value.copy(isDtcLoading = true)
                val result = OBDProtocol.clearAllDtcsUds(elm, upperKey, createListener())

                when (result) {
                    OBDProtocol.KeySubmitResult.SUCCESS -> {
                        saveKey(upperKey)
                        // Re-read to verify
                        kotlinx.coroutines.delay(1000)
                        OBDProtocol.readAllDtcs(elm, createListener())

                        if (_state.value.dtcList.isEmpty()) {
                            _state.value = _state.value.copy(
                                ecuResponse = "✓ All DTCs cleared!",
                                ecuResponseCategory = UdsResponse.Category.SUCCESS
                            )
                        } else {
                            _state.value = _state.value.copy(
                                ecuResponse = "${_state.value.dtcList.size} DTC(s) remain",
                                ecuResponseCategory = UdsResponse.Category.WARNING
                            )
                        }
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.CONNECTED,
                            isDtcLoading = false,
                            pendingDtcClear = false
                        )
                    }
                    OBDProtocol.KeySubmitResult.INVALID_KEY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Enter security key for DTC clear",
                            isDtcLoading = false
                        )
                    }
                    OBDProtocol.KeySubmitResult.TIME_DELAY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Wait then retry",
                            isDtcLoading = false
                        )
                    }
                    OBDProtocol.KeySubmitResult.MAX_ATTEMPTS -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR,
                            isDtcLoading = false,
                            pendingDtcClear = false
                        )
                    }
                    OBDProtocol.KeySubmitResult.REJECTED -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR,
                            isDtcLoading = false,
                            pendingDtcClear = false
                        )
                    }
                }
            } else if (_state.value.pendingKeyTest) {
                // Key Test flow: unlock ONLY, verify response, disconnect
                val result = OBDProtocol.testKeyValidity(elm, upperKey, createListener())

                when (result) {
                    OBDProtocol.KeySubmitResult.SUCCESS -> {
                        saveKey(upperKey)
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.CONNECTED,
                            pendingKeyTest = false,
                            ecuResponse = "✓ Key accepted by ECU!",
                            ecuResponseCategory = UdsResponse.Category.SUCCESS
                        )
                    }
                    OBDProtocol.KeySubmitResult.INVALID_KEY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Key rejected, try again"
                        )
                    }
                    OBDProtocol.KeySubmitResult.TIME_DELAY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Wait then retry"
                        )
                    }
                    OBDProtocol.KeySubmitResult.MAX_ATTEMPTS -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR,
                            pendingKeyTest = false
                        )
                    }
                    OBDProtocol.KeySubmitResult.REJECTED -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR,
                            pendingKeyTest = false
                        )
                    }
                }
            } else {
                // SHVCS clear flow: original unlock + clear sequence
                val result = OBDProtocol.executeUnlockAndClear(elm, upperKey, createListener())

                when (result) {
                    OBDProtocol.KeySubmitResult.SUCCESS -> {
                        saveKey(upperKey)
                    }
                    OBDProtocol.KeySubmitResult.INVALID_KEY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Enter security key"
                        )
                    }
                    OBDProtocol.KeySubmitResult.TIME_DELAY -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.AWAITING_KEY,
                            progressStep = "Wait then retry"
                        )
                    }
                    OBDProtocol.KeySubmitResult.MAX_ATTEMPTS -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR
                        )
                    }
                    OBDProtocol.KeySubmitResult.REJECTED -> {
                        _state.value = _state.value.copy(
                            connectionState = ConnectionState.ERROR
                        )
                    }
                }
            }
        }
    }

