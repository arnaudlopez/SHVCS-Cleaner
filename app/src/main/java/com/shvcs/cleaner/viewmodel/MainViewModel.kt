enum class ConnectionState {
    DISCONNECTED, CONNECTING, CONNECTED, RUNNING, AWAITING_KEY, UNLOCKING, SUCCESS, ERROR
}

enum class ConnectionMode { WIFI, BLUETOOTH }

data class AppState(
    val connectionState: ConnectionState = ConnectionState.DISCONNECTED,
    val vin: String = "—",
    val voltage: String = "—",
    val seed: String = "—",
    val progress: Float = 0f,
    val progressStep: String = "",
    val totalSteps: Int = 0,
    val currentStep: Int = 0,
    val logs: List<String> = emptyList(),
    val errorMessage: String? = null,
    val elmIp: String = ElmWifiManager.DEFAULT_IP,
    val elmPort: Int = ElmWifiManager.DEFAULT_PORT,
    val savedKeys: List<String> = emptyList(),
    /** Human-readable summary of the last ECU response */
    val ecuResponse: String? = null,
    /** Color category for the ECU response display */
    val ecuResponseCategory: UdsResponse.Category = UdsResponse.Category.NEUTRAL,
    /** List of DTCs read from the ECU */
    val dtcList: List<DtcParser.DtcCode> = emptyList(),
    /** Whether a DTC read/clear operation is in progress */
    val isDtcLoading: Boolean = false,
    /** Seed → Key mappings configured in Settings */
    val seedKeyMap: Map<String, String> = emptyMap(),
    /** Key automatically calculated from the Voltage reverse-engineered DB */
    val autoGeneratedKey: String? = null,
    /** True when the AWAITING_KEY state is for testing the key instead of clearing */
    val pendingKeyTest: Boolean = false,
    /** True when the AWAITING_KEY state is for DTC clear (vs SHVCS clear) */
    val pendingDtcClear: Boolean = false,
    /** Console history: list of (command, response) pairs */
    val consoleHistory: List<Pair<String, String>> = emptyList(),
    /** ECUs discovered during init scan */
    val discoveredEcus: List<OBDProtocol.DiscoveredEcu> = emptyList(),
    /** Battery data from the latest scan */
    val batteryData: BatteryDataParser.BatteryData? = null,
    /** Whether a battery scan is in progress */
    val isBatteryScanLoading: Boolean = false,
    /** Whether live monitoring is active */
    val isMonitoring: Boolean = false,
    /** Live monitoring data history (for charts) */
    val liveHistory: List<LiveMonitor.LiveDataPoint> = emptyList(),
    /** Persisted scan history from Room DB */
    val scanHistory: List<ScanResult> = emptyList(),
    /** Compare tab: selected scan A */
    val compareScanA: ScanResult? = null,
    /** Compare tab: selected scan B */
    val compareScanB: ScanResult? = null,
    /** Currently selected navigation tab */
    val selectedTab: Int = 0,
    /** Multi-module DTC scan result */
    val dtcScanResult: DtcScanner.DtcScanResult? = null,
    /** Module currently being cleared (null if none) */
    val clearingModuleId: String? = null,
    /** Connection mode: WiFi or Bluetooth */
    val connectionMode: ConnectionMode = ConnectionMode.WIFI,
    /** Connected Bluetooth device name (if applicable) */
    val btDeviceName: String? = null,
)

class MainViewModel(application: Application) : AndroidViewModel(application) {

    private val wifiElm = ElmWifiManager()
    private val btElm = ElmBluetoothManager()
    private var elm: ElmManager = wifiElm
    private val _state = MutableStateFlow(AppState())
    val state: StateFlow<AppState> = _state.asStateFlow()
    private var liveJob: Job? = null
    private val db = ScanResultDatabase.getInstance(application)

    private val prefs = application.getSharedPreferences("shvcs_settings", Context.MODE_PRIVATE)

    init {
        SeedKeyGenerator.initialize(application)
        _state.value = _state.value.copy(
            elmIp = prefs.getString("elm_ip", ElmWifiManager.DEFAULT_IP) ?: ElmWifiManager.DEFAULT_IP,
            elmPort = prefs.getInt("elm_port", ElmWifiManager.DEFAULT_PORT),
            savedKeys = loadSavedKeys(),
            seedKeyMap = loadSeedKeyMap()
        )
        // Collect scan history from Room
        viewModelScope.launch {
            db.scanResultDao().getAllDesc().collect { scans ->
                _state.value = _state.value.copy(scanHistory = scans)
            }
        }
    }

    fun saveSettings(ip: String, port: Int) {
        prefs.edit()
            .putString("elm_ip", ip)
            .putInt("elm_port", port)
            .apply()
        _state.value = _state.value.copy(elmIp = ip, elmPort = port)
    }

    /**
     * Save a seed → key mapping.
     */
    fun saveSeedKeyMapping(seed: String, key: String) {
        val upperSeed = seed.uppercase()
        val upperKey = key.uppercase()
        val map = _state.value.seedKeyMap.toMutableMap()
        map[upperSeed] = upperKey
        _state.value = _state.value.copy(seedKeyMap = map)
        persistSeedKeyMap(map)
    }

    /**
     * Delete a seed → key mapping.
     */
    fun deleteSeedKeyMapping(seed: String) {
        val map = _state.value.seedKeyMap.toMutableMap()
        map.remove(seed.uppercase())
        _state.value = _state.value.copy(seedKeyMap = map)
        persistSeedKeyMap(map)
    }

    fun connect() {
        viewModelScope.launch {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.CONNECTING,
                logs = _state.value.logs + "Connecting to ${_state.value.elmIp}:${_state.value.elmPort}...",
                errorMessage = null
            )
            val result = wifiElm.connect(_state.value.elmIp, _state.value.elmPort)
            result.onSuccess {
                _state.value = _state.value.copy(
                    logs = _state.value.logs + "✓ TCP connected to ELM327"
                )

                // Auto-init and read vehicle data
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.RUNNING,
                    progressStep = "Initializing..."
                )
                val initOk = OBDProtocol.executeInitAndReadData(elm, createListener())

                if (initOk) {
                    _state.value = _state.value.copy(
                        connectionState = ConnectionState.CONNECTED,
                        progress = 1f,
                        progressStep = "Ready"
                    )

                    // Auto-scan battery data (kept as requested)
                    scanBattery()
                } else {
                    // TCP is still connected, but init failed — show as CONNECTED with error
                    _state.value = _state.value.copy(
                        connectionState = ConnectionState.CONNECTED,
                        progress = 0f,
                        progressStep = "Init failed",
                        errorMessage = "ELM327 initialization failed — try again or check connection"
                    )
                }
            }.onFailure { e ->
                // TCP connection failed — go back to DISCONNECTED so the UI shows CONNECT
                elm.disconnect()
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.DISCONNECTED,
                    logs = _state.value.logs + "✗ Connection failed: ${e.message}",
                    errorMessage = "Connection failed: ${e.message}"
                )
            }
        }
    }

