    /**
     * Start live monitoring — polls battery data continuously.
     */
    fun startLiveMonitoring() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        if (_state.value.isMonitoring) return

        _state.value = _state.value.copy(
            isMonitoring = true,
            liveHistory = emptyList(),
            logs = _state.value.logs + "" + "━━━ STARTING LIVE MONITORING ━━━"
        )

        val listener = object : LiveMonitor.Listener {
            override fun onLog(message: String) {
                _state.value = _state.value.copy(logs = _state.value.logs + message)
            }
            override fun onError(error: String) {
                _state.value = _state.value.copy(
                    logs = _state.value.logs + "✗ Live error: $error"
                )
            }
        }

        liveJob = viewModelScope.launch {
            LiveMonitor.start(elm, LiveMonitor.Config(), listener).collect { point ->
                val history = _state.value.liveHistory.toMutableList()
                history.add(point)
                // Trim to max size
                while (history.size > 300) history.removeAt(0)

                _state.value = _state.value.copy(
                    batteryData = point.data,
                    liveHistory = history
                )
            }
        }
    }

    /**
     * Stop live monitoring.
     */
    fun stopLiveMonitoring() {
        liveJob?.cancel()
        liveJob = null
        _state.value = _state.value.copy(
            isMonitoring = false,
            logs = _state.value.logs + "━━━ LIVE MONITORING STOPPED ━━━"
        )
    }

    /**
     * Read all DTCs from the ECU (requires connection).
     */
    fun readDtcs() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            _state.value = _state.value.copy(
                isDtcLoading = true,
                errorMessage = null,
                dtcList = emptyList()
            )
            OBDProtocol.readAllDtcs(elm, createListener())
            _state.value = _state.value.copy(isDtcLoading = false)
        }
    }

    /**
     * Clear DTCs using OBD-II Mode 04 (generic, no security needed), then re-read.
     */
    fun clearDtcsGeneric() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            _state.value = _state.value.copy(
                isDtcLoading = true,
                errorMessage = null,
                pendingDtcClear = false
            )

            val ok = OBDProtocol.clearAllDtcsGeneric(elm, createListener())

            // Re-read to verify
            kotlinx.coroutines.delay(1000)
            OBDProtocol.readAllDtcs(elm, createListener())

            if (ok) {
                if (_state.value.dtcList.isEmpty()) {
                    _state.value = _state.value.copy(
                        ecuResponse = "✓ Generic DTCs cleared!",
                        ecuResponseCategory = UdsResponse.Category.SUCCESS
                    )
                } else {
                    _state.value = _state.value.copy(
                        ecuResponse = "${_state.value.dtcList.size} DTC(s) remain",
                        ecuResponseCategory = UdsResponse.Category.WARNING
                    )
                }
            } else {
                _state.value = _state.value.copy(
                    ecuResponse = "Generic DTC clear failed",
                    ecuResponseCategory = UdsResponse.Category.ERROR
                )
            }
            _state.value = _state.value.copy(isDtcLoading = false)
        }
    }

    /**
     * Clear DTCs using UDS Service 0x14 on HCPM2 (requires security access).
     * Uses seed-key mappings for auto-submit, or shows key dialog.
     */
    fun clearDtcsUds() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            _state.value = _state.value.copy(
                isDtcLoading = true,
                errorMessage = null,
                pendingDtcClear = true
            )

            // Request seed on HCPM2
            val seed = OBDProtocol.requestSeedForDtcClear(elm, createListener())

            if (seed != null) {
                // Look up key in saved mappings
                val knownKey = _state.value.seedKeyMap[seed.uppercase()]
                if (knownKey != null) {
                    // Auto-submit: key found in mappings
                    _state.value = _state.value.copy(
                        logs = _state.value.logs + "✓ Found saved key for seed $seed → auto-submitting for DTC clear"
                    )
                    val result = OBDProtocol.clearAllDtcsUds(elm, knownKey, createListener())
                    if (result == OBDProtocol.KeySubmitResult.SUCCESS) {
                        saveKey(knownKey)
                    }

                    // Re-read to verify
                    kotlinx.coroutines.delay(1000)
                    OBDProtocol.readAllDtcs(elm, createListener())

                    if (_state.value.dtcList.isEmpty()) {
                        _state.value = _state.value.copy(
                            ecuResponse = "✓ UDS DTCs cleared!",
                            ecuResponseCategory = UdsResponse.Category.SUCCESS
                        )
                    } else {
                        _state.value = _state.value.copy(
                            ecuResponse = "${_state.value.dtcList.size} DTC(s) remain",
                            ecuResponseCategory = UdsResponse.Category.WARNING
                        )
                    }
                    _state.value = _state.value.copy(isDtcLoading = false, pendingDtcClear = false)
                } else {
                    // No key found — try universal generator
                    val generated = SeedKeyGenerator.generateKey(seed)
                    
                    _state.value = _state.value.copy(
                        connectionState = ConnectionState.AWAITING_KEY,
                        seed = seed,
                        progressStep = if (generated != null) "Press RUN again to unlock" else "Enter security key for DTC clear",
                        isDtcLoading = false,
                        autoGeneratedKey = generated,
                        logs = if (generated != null) _state.value.logs + "✓ Universal generator found key: $generated" else _state.value.logs
                    )
                    // submitKey() will handle the rest when pendingDtcClear=true
                }
            } else {
                // Seed request failed
                _state.value = _state.value.copy(
                    isDtcLoading = false,
                    pendingDtcClear = false,
                    ecuResponse = "UDS DTC clear failed — could not get seed",
                    ecuResponseCategory = UdsResponse.Category.ERROR
                )
            }
        }
    }

    /**
     * Scan DTCs from all ECU modules using UDS Service 19 02 (multi-module).
     */
    fun scanDtcsMultiModule() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            _state.value = _state.value.copy(
                isDtcLoading = true,
                errorMessage = null,
                dtcScanResult = null
            )
            
            val listener = object : DtcScanner.Listener {
                override fun onLog(message: String) {
                    _state.value = _state.value.copy(logs = _state.value.logs + message)
                }
                override fun onProgress(step: Int, totalSteps: Int, description: String) {
                    _state.value = _state.value.copy(
                        progressStep = description,
                        currentStep = step,
                        totalSteps = totalSteps,
                        progress = step.toFloat() / totalSteps
                    )
                }
                override fun onModuleScanned(module: DtcScanner.ModuleDtcs) {
                    // Update result incrementally
                    val current = _state.value.dtcScanResult
                    val modules = (current?.modules ?: emptyList()) + module
                    _state.value = _state.value.copy(
                        dtcScanResult = DtcScanner.DtcScanResult(
                            modules = modules,
                            totalDtcCount = modules.sumOf { it.dtcs.size }
                        )
                    )
                }
                override fun onScanComplete(result: DtcScanner.DtcScanResult) {
                    _state.value = _state.value.copy(
                        dtcScanResult = result,
                        // Also update legacy dtcList with all DTCs flattened
                        dtcList = result.modules.flatMap { it.dtcs }
                    )
                }
                override fun onError(error: String) {
                    _state.value = _state.value.copy(
                        errorMessage = error,
                        logs = _state.value.logs + "✗ ERROR: $error"
                    )
                }
            }
            
            DtcScanner.scanAllModules(elm, listener)
            _state.value = _state.value.copy(isDtcLoading = false)
        }
    }

    /**
     * Clear DTCs on a specific module using UDS Service 14, then rescan that module.
     */
    fun clearModuleDtcs(moduleId: String, moduleName: String) {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            _state.value = _state.value.copy(
                clearingModuleId = moduleId,
                errorMessage = null
            )
            
            val listener = object : DtcScanner.Listener {
                override fun onLog(message: String) {
                    _state.value = _state.value.copy(logs = _state.value.logs + message)
                }
                override fun onProgress(step: Int, totalSteps: Int, description: String) {}
                override fun onModuleScanned(module: DtcScanner.ModuleDtcs) {}
                override fun onScanComplete(result: DtcScanner.DtcScanResult) {}
                override fun onError(error: String) {
                    _state.value = _state.value.copy(
                        errorMessage = error,
                        logs = _state.value.logs + "✗ ERROR: $error"
                    )
                }
            }
            
            val success = DtcScanner.clearModuleDtcs(elm, moduleId, moduleName, listener)
            
            if (success) {
                _state.value = _state.value.copy(
                    ecuResponse = "✓ DTCs cleared on $moduleName!",
                    ecuResponseCategory = UdsResponse.Category.SUCCESS,
                    logs = _state.value.logs + "✓ DTCs cleared on $moduleName — rescanning..."
                )
                
                // Rescan all modules to refresh
                kotlinx.coroutines.delay(1000)
                _state.value = _state.value.copy(clearingModuleId = null, isDtcLoading = true)
                DtcScanner.scanAllModules(elm, listener)
                _state.value = _state.value.copy(isDtcLoading = false)
            } else {
                _state.value = _state.value.copy(
                    ecuResponse = "Clear failed on $moduleName",
                    ecuResponseCategory = UdsResponse.Category.ERROR,
                    clearingModuleId = null
                )
            }
        }
    }

    private fun createListener() = object : OBDProtocol.Listener {
        override fun onLog(message: String) {
            _state.value = _state.value.copy(logs = _state.value.logs + message)
        }
        override fun onVinReceived(vin: String) {
            _state.value = _state.value.copy(vin = vin)
        }
        override fun onVoltageReceived(voltage: String) {
            _state.value = _state.value.copy(voltage = voltage)
        }
        override fun onSeedReceived(seed: String) {
            _state.value = _state.value.copy(seed = seed)
        }
        override fun onProgress(step: Int, totalSteps: Int, description: String) {
            _state.value = _state.value.copy(
                progress = step.toFloat() / totalSteps.toFloat(),
                currentStep = step, totalSteps = totalSteps,
                progressStep = description
            )
        }
        override fun onAwaitingKey(seed: String) {
            // Check for auto-submit: if we have a known key for this seed
            val knownKey = _state.value.seedKeyMap[seed.uppercase()]
            if (knownKey != null) {
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.AWAITING_KEY,
                    progressStep = "Auto-submitting key for seed $seed",
                    logs = _state.value.logs + "✓ Found saved key for seed $seed → auto-submitting"
                )
                // Auto-submit the key
                submitKey(knownKey)
            } else {
                val generated = SeedKeyGenerator.generateKey(seed)
                
                _state.value = _state.value.copy(
                    connectionState = ConnectionState.AWAITING_KEY,
                    progressStep = if (generated != null) "Press SUBMIT to unlock" else "Enter security key",
                    autoGeneratedKey = generated,
                    logs = if (generated != null) _state.value.logs + "✓ Universal generator found key: $generated" else _state.value.logs
                )
            }
        }
        override fun onUnlockSuccess() {
            _state.value = _state.value.copy(
                logs = _state.value.logs + "✓ HCPM2 Unlocked"
            )
        }
        override fun onClearSuccess() {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.SUCCESS,
                progress = 1f,
                progressStep = "SHVCS ERROR CLEARED!"
            )
        }
        override fun onClearFailed(reason: String) {
            _state.value = _state.value.copy(
                connectionState = ConnectionState.ERROR,
                errorMessage = reason
            )
        }
        override fun onError(error: String) {
            // Only log the error — don't change connection state
            // (connection state is managed by the calling functions)
            _state.value = _state.value.copy(
                errorMessage = error,
                logs = _state.value.logs + "✗ ERROR: $error"
            )
        }
        override fun onEcuResponse(response: UdsResponse) {
            _state.value = _state.value.copy(
                ecuResponse = response.toDisplayString(),
                ecuResponseCategory = response.category()
            )
        }
        override fun onDtcsRead(dtcs: List<DtcParser.DtcCode>) {
            _state.value = _state.value.copy(dtcList = dtcs)
        }
        override fun onEcusDiscovered(ecus: List<OBDProtocol.DiscoveredEcu>) {
            _state.value = _state.value.copy(discoveredEcus = ecus)
        }
    }

    private fun saveKey(key: String) {
        val existing = _state.value.savedKeys.toMutableList()
        existing.remove(key) // Remove if exists to put at top
        existing.add(0, key) // Add at top
        if (existing.size > 10) existing.removeAt(existing.lastIndex)
        _state.value = _state.value.copy(savedKeys = existing)
        prefs.edit().putStringSet("saved_keys", existing.toSet()).apply()
    }

    private fun loadSavedKeys(): List<String> {
        return prefs.getStringSet("saved_keys", emptySet())?.toList() ?: emptyList()
    }

    private fun loadSeedKeyMap(): Map<String, String> {
        val json = prefs.getString("seed_key_map", null) ?: return emptyMap()
        return try {
            val obj = JSONObject(json)
            val map = mutableMapOf<String, String>()
            obj.keys().forEach { key -> map[key] = obj.getString(key) }
            map
        } catch (_: Exception) { emptyMap() }
    }

    private fun persistSeedKeyMap(map: Map<String, String>) {
        val obj = JSONObject()
        map.forEach { (seed, key) -> obj.put(seed, key) }
        prefs.edit().putString("seed_key_map", obj.toString()).apply()
    }

    /**
     * Send a raw command to the ELM327, optionally setting a target ECU header first.
     */
    fun sendRawCommand(command: String, ecuHeader: String? = null) {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        viewModelScope.launch {
            try {
                val history = _state.value.consoleHistory.toMutableList()

                // Set header if specified
                if (!ecuHeader.isNullOrBlank()) {
                    val shCmd = "ATSH$ecuHeader"
                    history.add(shCmd to "...")
                    _state.value = _state.value.copy(consoleHistory = history.toList())

                    val shResult = elm.sendCommand(shCmd, timeoutMs = 3000L)
                    shResult.onSuccess { resp ->
                        history[history.lastIndex] = shCmd to resp.trim()
                    }.onFailure { e ->
                        history[history.lastIndex] = shCmd to "ERROR: ${e.message}"
                    }
                    _state.value = _state.value.copy(
                        consoleHistory = history.toList(),
                        logs = _state.value.logs + "► CONSOLE: $shCmd → ${history.last().second}"
                    )
                    kotlinx.coroutines.delay(100)
                }

                // Send the actual command
                history.add(command to "...")
                _state.value = _state.value.copy(consoleHistory = history.toList())

                val result = elm.sendCommand(command, timeoutMs = 10000L)
                result.onSuccess { resp ->
                    history[history.lastIndex] = command to resp.trim()
                }.onFailure { e ->
                    history[history.lastIndex] = command to "ERROR: ${e.message}"
                }
                _state.value = _state.value.copy(
                    consoleHistory = history.toList(),
                    logs = _state.value.logs + "► CONSOLE: $command → ${history.last().second}"
                )

            } catch (e: Exception) {
                _state.value = _state.value.copy(
                    logs = _state.value.logs + "✗ Console error: ${e.message}"
                )
            }
        }
    }

    fun clearConsoleHistory() {
        _state.value = _state.value.copy(consoleHistory = emptyList())
    }

    override fun onCleared() {
        super.onCleared()
        viewModelScope.launch { elm.disconnect() }
    }
