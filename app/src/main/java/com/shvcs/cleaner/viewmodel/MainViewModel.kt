    /**
     * Execute a full battery scan via HPCM2.
     * Configures CAN for HPCM2 header, reads all battery PIDs, and parses data.
     */
    fun scanBattery() {
        if (_state.value.connectionState == ConnectionState.DISCONNECTED) return
        if (_state.value.isBatteryScanLoading) return

        viewModelScope.launch {
            _state.value = _state.value.copy(
                isBatteryScanLoading = true,
                errorMessage = null,
                logs = _state.value.logs + "" + "━━━ STARTING BATTERY SCAN ━━━"
            )

            val batteryListener = object : BatteryScanner.Listener {
                override fun onLog(message: String) {
                    _state.value = _state.value.copy(logs = _state.value.logs + message)
                }
                override fun onProgress(step: Int, totalSteps: Int, description: String) {
                    _state.value = _state.value.copy(
                        progress = step.toFloat() / totalSteps.toFloat(),
                        progressStep = description
                    )
                }
                override fun onBatteryData(data: BatteryDataParser.BatteryData) {
                    _state.value = _state.value.copy(batteryData = data)
                }
                override fun onError(error: String) {
                    _state.value = _state.value.copy(
                        errorMessage = error,
                        logs = _state.value.logs + "✗ Battery scan error: $error"
                    )
                }
            }

            BatteryScanner.scanBattery(elm, batteryListener)

            // Auto-save scan result to Room DB
            val bd = _state.value.batteryData
            if (bd != null) {
                val scanResult = ScanResult(
                    socDisplayed = bd.socDisplayed,
                    socRaw = bd.socRaw,
                    evRange = bd.evRange,
                    hvVoltage = bd.hvVoltage,
                    hvAmperage = bd.hvAmperage,
                    hvPower = bd.hvPower,
                    batteryTemp = bd.batteryTemp,
                    ambientTemp = bd.ambientTemp,
                    capacity = bd.capacityAh,
                    isolation = bd.isolationKohm,
                    cellDelta = bd.cellDelta,
                    chargerAcPower = bd.chargerAcPower,
                    chargerHvPower = bd.chargerHvPower,
                    cellVoltages = bd.cellVoltages,
                    heaterOn = bd.activeHeating,
                    coolingOn = bd.activeCooling,
                    heaterPower = bd.batteryHeaterPower,
                    voltage12v = _state.value.voltage,
                    odometerKm = bd.odometerKm,
                    vehicleSpeed = bd.vehicleSpeed,
                    engineRpm = bd.engineRpm,
                    coolantTemp = bd.coolantTemp,
                    chargerTemp1 = bd.chargerTemp1,
                    chargerTemp2 = bd.chargerTemp2
                )
                db.scanResultDao().insert(scanResult)
                _state.value = _state.value.copy(
                    logs = _state.value.logs + "✓ Scan saved to history"
                )
            }

            _state.value = _state.value.copy(
                isBatteryScanLoading = false,
                progressStep = if (_state.value.batteryData != null) "Battery scan complete" else "Battery scan failed"
            )
        }
    }

